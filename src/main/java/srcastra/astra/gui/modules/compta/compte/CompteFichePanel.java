    public void setFields() {

        grp_Tfield_lib2.setText(compte.getLibelle2());

        grp_Tfield_lib1.setText(compte.getLibelle1());

        grp_Tfield_numero.setText("" + compte.getCe_num());

        grp_AC_lettrable.setSelected(compte.getLettrable() == 1 ? true : false);

        grp_Liste_dc.setCleUnik(compte.getDc());

        grp_Liste_tva.setCleUnik(compte.getTva_cleunik());


    }


    public void updateFields() {

        if (compte == null)

            compte = new srcastra.astra.sys.classetransfert.configuration.Compte();

        compte.setOldce_num(compte.getCe_num());

        compte.setLibelle1(grp_Tfield_lib1.getText());

        compte.setLibelle2(grp_Tfield_lib2.getText());

        try {

            compte.setCe_num(Long.parseLong(grp_Tfield_numero.getText()));

        } catch (NumberFormatException nn) {

            return;

        }

        compte.setCategorie(0);

        compte.setAnalityque(0);

        compte.setDc(grp_Liste_dc.getCleUnik());

        compte.setTva_cleunik(grp_Liste_tva.getCleUnik());

    }


